/**
 * Get all phrases from all categories
 * @returns {Array} Array of all phrases with category info
 */
export function getAllPhrases() {
    const allPhrases = [];
    Object.entries(categories).forEach(([categoryId, category]) => {
        category.phrases.forEach(phrase => {
            allPhrases.push({
                ...phrase,
                categoryId,
                categoryName: category.name
            });
        });
    });
    return allPhrases;
}

/**
 * Get phrases filtered by difficulty
 * @param {string} categoryId - Category to filter
 * @param {string|null} difficultyFilter - Difficulty filter
 * @returns {Array} Filtered phrases
 */
export function getFilteredPhrases(categoryId, difficultyFilter) {
    const category = categories[categoryId];
    if (!category) {
        return [];
    }

    if (!difficultyFilter) {
        return category.phrases;
    }

    switch (difficultyFilter) {
        case 'easy':
            return category.phrases.filter(p => p.difficulty === 'easy');
        case 'easy-medium':
            return category.phrases.filter(
                p => p.difficulty === 'easy' || p.difficulty === 'medium'
            );
        case 'medium':
            return category.phrases.filter(p => p.difficulty === 'medium');
        case 'hard':
            return category.phrases.filter(p => p.difficulty === 'hard');
        default:
            return category.phrases;
    }
}

/**
 * Get total phrase count
 * @returns {number} Total number of phrases
 */
export function getTotalPhraseCount() {
    return Object.values(categories).reduce((total, cat) => total + cat.phrases.length, 0);
}
